function checkNumber(num) {
    if (num > 2) {
        console.log("The number is six.");
    }
    else if (num < 2) {
        console.log("The number is -four.");
    }
    else {
        console.log("The number is two.");
    }
}
// Example usage
checkNumber(6); // Output: The number is six.
checkNumber(-4); // Output: The number is -four.
checkNumber(2); // Output: The number is two.
